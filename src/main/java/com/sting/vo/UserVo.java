package com.sting.vo;

public class UserVo {
	private int uid;
	private String id;
	private String phone;
	private String email;
	private String password;
	private String addrSiDo;
	private String addrDetail;
	private String addrNum;
	private String isPermit;
	private String accountNum;
	private String role;
	
	
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getAddrSiDo() {
		return addrSiDo;
	}
	public void setAddrSiDo(String addrSiDo) {
		this.addrSiDo = addrSiDo;
	}
	public String getAddrDetail() {
		return addrDetail;
	}
	public void setAddrDetail(String addrDetail) {
		this.addrDetail = addrDetail;
	}
	public String getAddrNum() {
		return addrNum;
	}
	public void setAddrNum(String addrNum) {
		this.addrNum = addrNum;
	}
	public String getIsPermit() {
		return isPermit;
	}
	public void setIsPermit(String isPermit) {
		this.isPermit = isPermit;
	}
	public String getAccountNum() {
		return accountNum;
	}
	public void setAccountNum(String accountNum) {
		this.accountNum = accountNum;
	}
	public String getRole() {
		return role;
	}
	public void setRole(String role) {
		this.role = role;
	}
	
}
