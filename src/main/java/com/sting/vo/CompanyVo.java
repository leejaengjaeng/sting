package com.sting.vo;

public class CompanyVo {
	private int cid;
	private int uid;
	private String registration_number;
	private String manager_name;
	private String manager_phonenumber;
	private String interestproduct;
	private String typeofcompany;
	private String company_name;
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public String getRegistration_number() {
		return registration_number;
	}
	public void setRegistration_number(String registration_number) {
		this.registration_number = registration_number;
	}
	public String getManager_name() {
		return manager_name;
	}
	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}
	public String getManager_phonenumber() {
		return manager_phonenumber;
	}
	public void setManager_phonenumber(String manager_phonenumber) {
		this.manager_phonenumber = manager_phonenumber;
	}
	public String getInterestproduct() {
		return interestproduct;
	}
	public void setInterestproduct(String interestproduct) {
		this.interestproduct = interestproduct;
	}
	public String getTypeofcompany() {
		return typeofcompany;
	}
	public void setTypeofcompany(String typeofcompany) {
		this.typeofcompany = typeofcompany;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	
}